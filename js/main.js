var Timeline = function(data) {
  this.data = data;
  this.dates_array = _.keys(this.data).sort();
  this.big_point = false;
  this.next_big_point = false;
  this.current_li = false;
  this.day_block_line_width = 261;
  this.current_year = false;
}

Timeline.prototype.run = function() {
  var that = this
  
  _.map(that.dates_array, function(item) {
    var current_item = {
      date: new Date(item*1000),
      text: that.data[item]
    };

    if (that.getNextBigPoint() &&
      that.matchPostItems(current_item, that.getNextBigPoint())) {
        that.setBigPoint(false);
    }

    if (!that.getBigPoint()) {
      that.setBigPoint(current_item.date);
      that.buildBigPoint(that.getBigPoint());
      that.current_year = that.getBigPoint().getFullYear();

      var next_day = that.findNextDayByBigPoint(that.getBigPoint())
      that.setNextBigPoint(new Date(next_day*1000));

      that.current_li = $('div.jcarousel ul li').last();
    }

    that.buildSmallPoint(current_item);

  });

  /* create last today big point */
  that.setBigPoint(new Date(that.getNextBigPoint()));
  that.buildBigPoint(new Date(that.getNextBigPoint()));
}


Timeline.prototype.matchPostItems = function(first_item, second_item) {
  if (first_item.date.getTime() === second_item.getTime()) {
    return true
  }
  return false;
}


Timeline.prototype.buildBigPoint = function(big_point) {
  var that = this;
  $('div.jcarousel ul').append('<li>' +
    '<div class="time-line-part">' +
      '<div class="year">' + (that.current_year != that.getBigPoint().getFullYear() ? that.getBigPoint().getFullYear() : "") + '</div>' + 
      '<div class="msg-block"><div></div>' +
        '<div class="msg-block-text"></div>' +
      '</div>' +
      '<div class="points-list">' +
        '<div class="point big-point">' +
          '<p class="point-date">' + that.getBigPoint().getDate() + '</p>' +
          '<p class="point-month">' + getRussianMonths()[that.getBigPoint().getMonth()] + '</p>' +
        '</div>' +
      '</div>' +
    '</div>' +
  '</li>');
}


Timeline.prototype.getNextBigPoint = function(big_point) {
  return this.next_big_point;
}


Timeline.prototype.setNextBigPoint = function(big_point) {
  this.next_big_point = big_point;
}


Timeline.prototype.getBigPoint = function(big_point) {
  return this.big_point;
}


Timeline.prototype.setBigPoint = function(big_point) {
  this.big_point = big_point;
}


Timeline.prototype.buildSmallPoint = function(small_point) {
  var that = this;
  that.current_li.find('div.points-list')
    .append('<div class="point small-point" style="left:' + that.getLeftPositionForPoint(small_point) + 'px;">' +
      '<div class="point-msg-corner"></div>' +
      '<div class="point-border"></div>' +
      '<div class="point-point"></div>' +
      '<div class="point-msg">' + small_point.text + '</div>' +
    '</div>');
}


Timeline.prototype.findNextDayByBigPoint = function(big_point) {
  var result = false;

  _.map(this.dates_array, function(item) {
    if (!result) {
      var count_days_between_dates = Math.floor(((item*1000) - big_point.getTime()) / (1000*60*60*24))
      if (count_days_between_dates > 1) {
        result = item;
      }
    }
  });

  if (!result) {
    var now = new Date();
    result = new Date(now.getFullYear(), now.getMonth(), now.getDate()).getTime() / 1000;
  }
  
  return result;
}


Timeline.prototype.findLastTimeOfDayByBigPoint = function(big_point) {
  var result = [];

  _.map(this.dates_array, function(item) {
    var count_days_between_dates = Math.floor(((item*1000) - big_point.getTime()) / (1000*60*60*24))
    if (count_days_between_dates < 1) {
      result.push(item);
    }
  });
  
  return result[result.length - 1];
}


Timeline.prototype.getLeftPositionForPoint = function(small_point) {
  var last_time_of_big_date_group = this.findLastTimeOfDayByBigPoint(this.getBigPoint());
  var differ_lst_time_bd_millsec = (last_time_of_big_date_group*1000) - this.getBigPoint().getTime();
  var differ_lst_time_currdat_millsec = small_point.date.getTime() - this.getBigPoint().getTime();
  var different_coeff = (differ_lst_time_currdat_millsec / differ_lst_time_bd_millsec) * 100;

  return this.day_block_line_width / 100 * different_coeff;
}








function getRussianMonths() {
  return ['Январь', 'Февраль', 'Март', 'Апрель', 'Май', 'Июнь', 'Июль', 'Август', 'Сентябрь', 'Октябрь', 'Ноябрь', 'Декабрь'];
}



